current_weather = 'cloudy'
case current_weather
when 'sunny'
    puts "Bring out your shorts and tank tops!"
when 'cloudy'
    puts "Looks like rain is coming, make sure you have a jacket with a hood."
when 'rainy'
    puts "It will be a wet day today but it will be hot. Don't forget your umbrella!"
else 
	puts "Wear whatever you want. Let's get crazy!!!!"
end